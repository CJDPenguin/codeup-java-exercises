public class StringExercise {

    public static void main(String[] args) {
        String pinkFloyd = "We don't need no education \nWe don't need no thought control";

        System.out.println(pinkFloyd);

        String weirdness = "Check \"this\" out!, \"s inside of s\"!";

        System.out.println(weirdness);

        String bootDrive = "In windows, the main drive is usually C:" + "/";

        System.out.println(bootDrive);

        String backslash = "\\";

        System.out.println("I can do backslashes " + backslash + ", double backslashes " + backslash + backslash + ", and the amazing triple backslash " + backslash + backslash + backslash + "!");
    }
}
